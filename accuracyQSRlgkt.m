function x=accuracyQSRlgkt(a,b,c,h,x1)
x = zeros(1, length(x1));
for i = 1:length(x1)-1
    x(i) = real(x1(i));
    if abs(x(i)) > 3
        x(i) = sign(x(i)) * 3;
    end
    k1 = (((a.^2).*(b.^2).*(c.^2).*x(i))-((a.^2).*(b.^2)-(b.^2).*(c.^2)+(c.^2).*(a.^2)).*x(i).^3+(a.^2+b.^2+c.^2).*x(i).^5-x(i).^7+x1(i));
    k2 = (((a.^2).*(b.^2).*(c.^2).*(x(i)+k1*h/2))-((a.^2).*(b.^2)-(b.^2).*(c.^2)+(c.^2).*(a.^2)).*(x(i)+k1*h/2).^3+(a.^2+b.^2+c.^2).*(x(i)+k1*h/2).^5-(x(i)+k1*h/2).^7+x1(i)+h/2);
    k3 = (((a.^2).*(b.^2).*(c.^2).*(x(i)+k2*h/2))-((a.^2).*(b.^2)-(b.^2).*(c.^2)+(c.^2).*(a.^2)).*(x(i)+k2*h/2).^3+(a.^2+b.^2+c.^2).*(x(i)+k2*h/2).^5-(x(i)+k2*h/2).^7+x1(i)+h/2);
    k4 = (((a.^2).*(b.^2).*(c.^2).*(x(i)+k3*h))-((a.^2).*(b.^2)-(b.^2).*(c.^2)+(c.^2).*(a.^2)).*(x(i)+k3*h).^3+(a.^2+b.^2+c.^2).*(x(i)+k3*h).^5-(x(i)+k3*h).^7+x1(i)+h);
    x(i+1) = x(i) + h*(k1 + 2*k2 + 2*k3 + k4)/6;
end
end
